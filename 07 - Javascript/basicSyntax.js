// Variables

// let - mutable or changeable variable
let myName = 'Kitu';
console.log(myName);

myName = 'Ico';
console.log(myName);

// const - unchangeable variable
const myYear = 2024;
console.log(myYear);

// myYear = 2025; - will give an error
// console.log(myYear); 

// var - use for function arguments
var myHobby = 'sleeping';
console.log(myHobby);

myHobby = 'eating';
console.log(myHobby);


