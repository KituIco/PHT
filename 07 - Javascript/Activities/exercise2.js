// We initialize variable kelvin as a constant variable with a value 293.
const kelvin = 293;


// We store into the variable celsius the value when 273 is subtracted from the variable kelvin.
const celsius = kelvin - 273;

// We store into the variable farenheit the converted value given by the formula celsius * (9/5) + 32.
const fahrenheit = celsius * (9/5) + 32;

// We print fahrenheit.
console.log(fahrenheit);